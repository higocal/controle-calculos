CREATE TABLE USUARIO (
    ID INTEGER PRIMARY KEY AUTO_INCREMENT,
    NOME VARCHAR(100)
    SALDO NUMERIC(19,2)
);

CREATE TABLE RECEITA (
    ID INTEGER PRIMARY KEY AUTO_INCREMENT,
    DESCRICAO VARCHAR(100),
    DATA_RECEITA DATE,
    VALOR_RECEITA NUMERIC(20,2)
);

CREATE TABLE DESPESA (
    ID INTEGER PRIMARY KEY AUTO_INCREMENT,
    DESCRICAO VARCHAR(100),
    DATA_VENCIMENTO DATE,
    VALOR_DESPESA NUMERIC(20,2)
);
